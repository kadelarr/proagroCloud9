				<h2>Acerca de Nosotros</h2>
				<p>La empresa es una hacienda donde se cultiva caña de
					azúcar para la producción de panela con más de
					50 años en el negocio; hace 12 años decidimos convertirnos
					en Productos Agroindustriales S.A. con el deseo de crecer y
					fortalecer el desarrollo de la región. Actualmente nos
					encontramos ubicados en el municipio de la Tebaida en el
					Departamento del Quindío, región cafetera de
					Colombia, contamos 40 empleados que día a día colocan
					todo su empeño en la elaboración de la mejor panela que se
					pueda disfrutar en frio o caliente.</p>

				<h2>Nuestra Misión</h2>
				<p>Producir y comercializar productos alimenticios basados en el
					azúcar y la panela, buscando así con esta actividad
					obtener excelentes resultados para sus socios. Además,
					procurar el desarrollo integral de sus empelados, contribuyendo a
					elevar la calidad de vida de todas las personas que hacen parte de
					la compañia directa o indirectamente
					comprometiéndolos día a día con los
					estándares de calidad y las buenas prácticas de
					manufactura, logrando así contribuir a la convivencia
					pacífica de la región y a su progreso en el tiempo.</p>
				<h2>Nuestra Visión</h2>
				<p>Hacia el 2020 queremos convertirnos en líderes de
					producción de productos derivados de caña de azúcar de alta
					calidad, ser modelo a corto plazo de empresa sostenible por su buen
					manejo y consolidación financiera e impulsar el desarrollo
					socioeconómico de la región.</p>


